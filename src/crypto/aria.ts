// Key Schedule
function keySchedule(key) {
  let KL = new Uint8Array(16);
  let KR = new Uint8Array(16);
  
  if (key.length === 16) {
    KL.set(key);
  } else if (key.length === 24) {
    KL.set(key.slice(0, 16));
    KR.set(key.slice(16, 24));
  } else if (key.length === 32) {
    KL.set(key.slice(0, 16));
    KR.set(key.slice(16, 32));
  } else {
    throw new Error('ARIA: invalid key size');
  }

  let CK1, CK2, CK3, rounds;
  if (key.length === 16) {
    CK1 = C1; CK2 = C2; CK3 = C3; rounds = 12;
  } else if (key.length === 24) {
    CK1 = C2; CK2 = C3; CK3 = C1; rounds = 14;
  } else {
    CK1 = C3; CK2 = C1; CK3 = C2; rounds = 16;
  }

  const W0 = KL;
  const W1 = xor128(FO(W0, CK1), KR);
  const W2 = xor128(FE(W1, CK2), W0);
  const W3 = xor128(FO(W2, CK3), W1);

  const ek = new Array(18); // 1-indexed up to 17
  ek[1]  = xor128(W0, rotr128(W1, 19));
  ek[2]  = xor128(W1, rotr128(W2, 19));
  ek[3]  = xor128(W2, rotr128(W3, 19));
  ek[4]  = xor128(rotr128(W0, 19), W3);
  ek[5]  = xor128(W0, rotr128(W1, 31));
  ek[6]  = xor128(W1, rotr128(W2, 31));
  ek[7]  = xor128(W2, rotr128(W3, 31));
  ek[8]  = xor128(rotr128(W0, 31), W3);
  ek[9]  = xor128(W0, rotl128(W1, 61));
  ek[10] = xor128(W1, rotl128(W2, 61));
  ek[11] = xor128(W2, rotl128(W3, 61));
  ek[12] = xor128(rotl128(W0, 61), W3);
  ek[13] = xor128(W0, rotl128(W1, 31));
  
  if (rounds >= 14) {
    ek[14] = xor128(W1, rotl128(W2, 31));
    ek[15] = xor128(W2, rotl128(W3, 31));
  }
  if (rounds === 16) {
    ek[16] = xor128(rotl128(W0, 31), W3);
    ek[17] = xor128(W0, rotl128(W1, 19));
  }

  const dk = new Array(rounds + 2);
  dk[1] = ek[rounds + 1];
  for (let i = 2; i <= rounds; i++) {
    dk[i] = A(ek[rounds + 2 - i]);
  }
  dk[rounds + 1] = ek[1];

  return { ek, dk, rounds };
}

// Block Encryption
function encryptBlock(block, ek, rounds) {
  let P = block;
  for (let i = 1; i <= rounds - 1; i++) {
    if (i % 2 === 1) {
      P = FO(P, ek[i]);
    } else {
      P = FE(P, ek[i]);
    }
  }
  P = xor128(SL2(xor128(P, ek[rounds])), ek[rounds + 1]);
  return P;
}

// Block Decryption
function decryptBlock(block, dk, rounds) {
  return encryptBlock(block, dk, rounds);
}

// Modes
function ecbEncrypt(key, data) {
  const { ek, rounds } = keySchedule(toBuf(key));
  const d = toBuf(data);
  const out = Buffer.alloc(d.length);
  for (let i = 0; i < d.length; i += 16) {
    const c = encryptBlock(d.slice(i, i + 16), ek, rounds);
    out.set(c, i);
  }
  return toHex(out);
}

function ecbDecrypt(key, data) {
  const { dk, rounds } = keySchedule(toBuf(key));
  const d = toBuf(data);
  const out = Buffer.alloc(d.length);
  for (let i = 0; i < d.length; i += 16) {
    const p = decryptBlock(d.slice(i, i + 16), dk, rounds);
    out.set(p, i);
  }
  return toHex(out);
}

function cbcEncrypt(key, data, iv) {
  const { ek, rounds } = keySchedule(toBuf(key));
  let prev = toBuf(iv);
  const d = toBuf(data);
  const out = Buffer.alloc(d.length);
  for (let i = 0; i < d.length; i += 16) {
    const p = xor128(d.slice(i, i + 16), prev);
    prev = encryptBlock(p, ek, rounds);
    out.set(prev, i);
  }
  return toHex(out);
}

function cbcDecrypt(key, data, iv) {
  const { dk, rounds } = keySchedule(toBuf(key));
  let prev = toBuf(iv);
  const d = toBuf(data);
  const out = Buffer.alloc(d.length);
  for (let i = 0; i < d.length; i += 16) {
    const c = d.slice(i, i + 16);
    const p = xor128(decryptBlock(c, dk, rounds), prev);
    out.set(p, i);
    prev = c;
  }
  return toHex(out);
}

// PKCS7 Padding
function fPad(hexData) {
  const buf = toBuf(hexData);
  const pad = 16 - (buf.length % 16);
  const out = Buffer.alloc(buf.length + pad);
  buf.copy(out);
  out.fill(pad, buf.length);
  return toHex(out);
}

function fUnpad(hexData) {
  const buf = toBuf(hexData);
  const pad = buf[buf.length - 1];
  if (pad < 1 || pad > 16) throw new Error('ARIA: invalid padding');
  for (let i = buf.length - pad; i < buf.length; i++) {
    if (buf[i] !== pad) throw new Error('ARIA: invalid padding');
  }
  return toHex(buf.slice(0, buf.length - pad));
}


