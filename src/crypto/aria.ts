// 128-bit array utils
function xor128(a, b) {
  const out = new Uint8Array(16);
  for (let i = 0; i < 16; i++) {
    out[i] = a[i] ^ b[i];
  }
  return out;
}

function copy128(a) {
  const out = new Uint8Array(16);
  out.set(a);
  return out;
}

function rotl128(a, n) {
  const out = new Uint8Array(16);
  const bytes = Math.floor(n / 8);
  const bits = n % 8;
  for (let i = 0; i < 16; i++) {
    const srcIdx = (i + bytes) % 16;
    const nextIdx = (i + bytes + 1) % 16;
    if (bits === 0) {
      out[i] = a[srcIdx];
    } else {
      out[i] = ((a[srcIdx] << bits) | (a[nextIdx] >>> (8 - bits))) & 0xFF;
    }
  }
  return out;
}

function rotr128(a, n) {
  return rotl128(a, 128 - (n % 128));
}

// Substitution Layers
function SL1(x) {
  const y = new Uint8Array(16);
  y[0]  = SB1[x[0]];  y[1]  = SB2[x[1]];  y[2]  = SB3[x[2]];  y[3]  = SB4[x[3]];
  y[4]  = SB1[x[4]];  y[5]  = SB2[x[5]];  y[6]  = SB3[x[6]];  y[7]  = SB4[x[7]];
  y[8]  = SB1[x[8]];  y[9]  = SB2[x[9]];  y[10] = SB3[x[10]]; y[11] = SB4[x[11]];
  y[12] = SB1[x[12]]; y[13] = SB2[x[13]]; y[14] = SB3[x[14]]; y[15] = SB4[x[15]];
  return y;
}

